// Local storage logic and dynamic text swapping
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("charity_lang", lang);
  
  // Swap all elements with data-tr attributes
  document.querySelectorAll("[data-tr]").forEach(el => {
    const key = el.getAttribute("data-tr");
    if (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) {
      el.textContent = TRANSLATIONS[lang][key];
    }
  });

  // Handle placeholders specifically
  const nameInput = document.getElementById("name");
  const emailInput = document.getElementById("email");
  const phoneInput = document.getElementById("phone");
  const msgInput = document.getElementById("message");
  const customDonationInput = document.getElementById("donation-amount");

  if (nameInput) nameInput.placeholder = lang === "en" ? "Enter your name" : "உங்கள் பெயர்";
  if (emailInput) emailInput.placeholder = lang === "en" ? "Enter your email" : "உங்கள் மின்னஞ்சல்";
  if (phoneInput) phoneInput.placeholder = lang === "en" ? "Enter your phone number" : "உங்கள் தொலைபேசி எண்";
  if (msgInput) msgInput.placeholder = lang === "en" ? "How can we help you?" : "நாங்கள் எவ்வாறு உதவலாம்?";
  if (customDonationInput) customDonationInput.placeholder = lang === "en" ? "Enter custom amount" : "தொகையை உள்ளிடவும்";

  // Update navbar indicator visual
  const langText = document.getElementById("lang-text");
  if (langText) {
    langText.textContent = lang === "en" ? "EN | த" : "த | EN";
  }
}

