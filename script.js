/* FETCH AND RENDER GALLERY PHOTOS */
let currentPhotos = [];

async function loadGallery() {
  const grid = document.getElementById("gallery-grid");
  const fallbackBanner = document.getElementById("fallback-banner");
  if (!grid) return;
  
  grid.innerHTML = `<div style="grid-column: 1/-1; text-align: center; padding: 3rem; color: var(--text-muted);">
    <i data-lucide="loader-2" class="animate-spin" style="width: 32px; height: 32px; margin: 0 auto 1rem auto; animation: spin 1.5s linear infinite;"></i>
    <p>Loading photos...</p>
  </div>`;
  
  if (typeof lucide !== "undefined") {
    lucide.createIcons();
  }

  let dbPhotos = [];
  let isSupabaseLoaded = false;
  
  // Try to use Supabase if config is provided
  if (typeof supabase !== "undefined" && SUPABASE_CONFIG.url && SUPABASE_CONFIG.anonKey) {
    try {
      const client = supabase.createClient(SUPABASE_CONFIG.url, SUPABASE_CONFIG.anonKey);
      const { data, error } = await client
        .from('gallery_photos')
        .select('*')
        .order('created_at', { ascending: false });
        
      if (!error && data) {
        dbPhotos = data;
        isSupabaseLoaded = true;
      }
    } catch (e) {
      console.warn("Supabase fetch failed. Loading mock gallery.", e);
    }
  }
  
  // Load mock data if Supabase isn't available
  if (!isSupabaseLoaded) {
    const deletedMocks = JSON.parse(localStorage.getItem("charity_deleted_mocks") || "[]");
    const activeMocks = MOCK_PHOTOS.map((m, idx) => ({ ...m, id: `mock-${idx + 1}` })).filter(m => !deletedMocks.includes(m.id));
    const customPhotos = JSON.parse(localStorage.getItem("charity_custom_photos") || "[]");
    dbPhotos = [...customPhotos, ...activeMocks];
    if (fallbackBanner) {
      fallbackBanner.style.display = "block";
    }
  } else {
    if (fallbackBanner) {
      fallbackBanner.style.display = "none";
    }
  }
  
  currentPhotos = dbPhotos;
  renderPhotos("all");
}

function renderPhotos(filter = "all") {
  const grid = document.getElementById("gallery-grid");
  if (!grid) return;
  
  grid.innerHTML = "";
  
  const filtered = filter === "all" 
    ? currentPhotos 
    : currentPhotos.filter(p => p.category === filter);
    
  if (filtered.length === 0) {
    grid.innerHTML = `<div style="grid-column: 1/-1; text-align: center; padding: 4rem; color: var(--text-muted);" data-tr="no_photos_found">
      No photos found in this category.
    </div>`;
    return;
  }
  
  filtered.forEach(photo => {
    const item = document.createElement("div");
    item.className = "gallery-item";
    item.setAttribute("data-aos", "zoom-in");
    
    // Map category name to clean printable label
    let catLabel = photo.category;
    if (photo.category === "blood") catLabel = "Blood Donation";
    if (photo.category === "cancer") catLabel = "Cancer Awareness";
    if (photo.category === "eye") catLabel = "Eye Camp";
    if (photo.category === "sapling") catLabel = "Sapling Drive";
    
    item.innerHTML = `
      <img src="${photo.url}" alt="${photo.caption || 'Charity Event'}" loading="lazy">
      <div class="gallery-overlay">
        <span class="gallery-item-category">${catLabel}</span>
        <h4 class="gallery-item-title">${photo.caption || 'Event'}</h4>
      </div>
    `;
    
    item.addEventListener("click", () => {
      openLightbox(photo.url, photo.caption || catLabel);
    });
    
    grid.appendChild(item);
  });
  
  // Re-trigger animations and icon creations
  if (typeof AOS !== "undefined") {
    AOS.refresh();
  }
}

// Lightbox Modal functions
const lightbox = document.getElementById("lightbox");
const lightboxImg = document.getElementById("lightbox-img");
const lightboxCaption = document.getElementById("lightbox-caption");
const lightboxClose = document.getElementById("lightbox-close");

function openLightbox(url, caption) {
  if (!lightbox) return;
  lightboxImg.src = url;
  lightboxCaption.textContent = caption;
  lightbox.style.display = "flex";
  document.body.style.overflow = "hidden"; // Disable background scrolling
}

function closeLightbox() {
  if (!lightbox) return;
  lightbox.style.display = "none";
  document.body.style.overflow = "auto";
}

if (lightboxClose) {
  lightboxClose.addEventListener("click", closeLightbox);
}
if (lightbox) {
  lightbox.addEventListener("click", (e) => {
    if (e.target === lightbox) closeLightbox();
  });
}

